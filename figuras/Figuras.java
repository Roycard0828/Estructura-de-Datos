public class Figuras {
    
    //Atributos 
    
    protected int base;
    protected int altura;
    protected int area;
    
    //Metodo constructor
    public Figuras(int base, int altura){
        this.base = base;
        this.altura = altura;
        
    }
    public Figuras(){
        
    }

    public int getBase() {
        return base;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
    
    public int getArea(){
        return area = 0;
    }
    
    
    
}
